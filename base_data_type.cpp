#include <iostream>
using namespace std;

int main() {
    short short1 = -10;
    unsigned short u_short1 = 10;
    int int1 = -100;
    unsigned int u_int1 = 100;
    long long1 = -1000;
    unsigned long u_long1 = 1000;

    cout << "short1 : " << short1 << endl;
    cout << "u_short1 : " << u_short1 << endl;
    cout << "int1 : " << int1 << endl;
    cout << "u_int1 : " << u_int1 << endl;
    cout << "long1 : " << long1 << endl;
    cout << "u_long1 : " << u_long1 << endl;

    double double1 = 10.1;
    float float1 = 12.22f;

    cout << "double1 : " << double1 << endl;
    cout << "float1 : " << float1 << endl;

    char char1 = 'a';

    cout << "char1 : " << char1 << endl;

    bool bool1 = true;
    bool bool2 = false;

    cout << "bool1 : " << bool1 << endl;
    cout << "bool2 : " << bool2 << endl;

    return 0;
}